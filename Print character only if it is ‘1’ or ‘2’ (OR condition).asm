.MODEL SMALL
.STACK 100H
.DATA
.CODE

MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    MOV AH, 01H
    INT 21H

    CMP AL, '1'
    JE PRINTCHAR
    CMP AL, '2'
    JE PRINTCHAR

    MOV AH, 4CH
    INT 21H

PRINTCHAR:
    MOV DL, AL
    MOV AH, 02H
    INT 21H

    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
